"float3 tetra_corner(float3 identity, float hue, float density) {\n"
"    float angle = hue * 1.047198f;\n"
"    float mn = (identity.x + identity.y + identity.z) / 3.0f;\n"
"    float cx = identity.x - mn;\n"
"    float cy = identity.y - mn;\n"
"    float cz = identity.z - mn;\n"
"    float cos_a = cos(angle);\n"
"    float sin_a = sin(angle);\n"
"    float k = 0.57735026919f;\n"
"    float rx = cx * cos_a + k * (cz - cy) * sin_a;\n"
"    float ry = cy * cos_a + k * (cx - cz) * sin_a;\n"
"    float rz = cz * cos_a + k * (cy - cx) * sin_a;\n"
"    return float3(rx + mn + density, ry + mn + density, rz + mn + density);\n"
"}\n"
