"    uint2 id [[thread_position_in_grid]])\n"
"{\n"
"    if ((int)id.x >= params.width || (int)id.y >= params.height) return;\n"
"    const int idx = ((id.y * params.width) + id.x) * 4;\n"
"\n"
"    float r = p_Input[idx + 0];\n"
"    float g = p_Input[idx + 1];\n"
"    float b = p_Input[idx + 2];\n"
"    float a = p_Input[idx + 3];\n"
"\n"
"    if (params.bypass) {\n"
"        p_Output[idx + 0] = r;\n"
"        p_Output[idx + 1] = g;\n"
"        p_Output[idx + 2] = b;\n"
"        p_Output[idx + 3] = a;\n"
"        return;\n"
"    }\n"
"\n"

// --- Stop chart ---
"    if (params.showChart) {\n"
"        int px = (int)id.x;\n"
"        int py = (int)id.y;\n"
"        float x_norm = (float)px / (float)params.width;\n"
"        float y_norm = 1.0f - (float)py / (float)params.height;\n"
"        float val;\n"
"        if (y_norm < 0.5f) {\n"
"            int num_cols = 17;\n"
"            int col = (int)(x_norm * (float)num_cols);\n"
"            if (col >= num_cols) col = num_cols - 1;\n"
"            int stop = col - 8;\n"
"            float lin = 0.18f * pow(2.0f, (float)stop);\n"
"            val = encode(lin, params.inputTF);\n"
"            val = max(0.0f, min(val, 1.0f));\n"
"        } else {\n"
"            val = x_norm;\n"
"            float mid_grey = encode(0.18f, params.inputTF);\n"
"            int cell = params.height / 150;\n"
"            if (cell < 2) cell = 2;\n"
"            int mg_d1 = ((int)(mid_grey * 10.0f)) % 10;\n"
"            int mg_d2 = ((int)(mid_grey * 100.0f)) % 10;\n"
"            int mg_d3 = ((int)(mid_grey * 1000.0f + 0.5f)) % 10;\n"
"            int char_w = 9 * cell;\n"
"            int char_h = 12 * cell;\n"
"            int line_gap = 4 * cell;\n"
"            int pad = 3 * cell;\n"
"            int name_len = tf_name_len(params.inputTF);\n"
"            int line2_len = 15;\n"
"            int ramp_h = params.height / 2;\n"
"            int total_text_h = char_h * 2 + line_gap;\n"
"            int line1_top = (ramp_h - total_text_h) / 2;\n"
"            int line2_top = line1_top + char_h + line_gap;\n"
"            int line1_left = (params.width - name_len * char_w + cell) / 2;\n"
"            int line2_left = (params.width - line2_len * char_w + cell) / 2;\n"
"            int box_l = (line1_left < line2_left ? line1_left : line2_left) - pad;\n"
"            int box_r = line1_left + name_len * char_w;\n"
"            int box_r2 = line2_left + line2_len * char_w;\n"
"            if (box_r2 > box_r) box_r = box_r2;\n"
"            box_r += pad;\n"
"            int box_t = line1_top - pad;\n"
"            int box_b = line2_top + char_h + pad;\n"
"            int in_box = (px >= box_l && px < box_r && py >= box_t && py < box_b) ? 1 : 0;\n"
"            int text_hit = 0;\n"
"            if (py >= line1_top && py < line1_top + char_h) {\n"
"                int lx = px - line1_left;\n"
"                if (lx >= 0 && lx < name_len * char_w) {\n"
"                    int ci = lx / char_w;\n"
"                    int cxx = (lx % char_w) / cell;\n"
"                    int cy = (py - line1_top) / cell;\n"
"                    if (cxx < 8) {\n"
"                        int ch = tf_name_char(params.inputTF, ci);\n"
"                        if (ch > 0) {\n"
"                            int mask = font_8x12(ch, cy);\n"
"                            if ((mask >> (7 - cxx)) & 1) text_hit = 1;\n"
"                        }\n"
"                    }\n"
"                }\n"
"            }\n"
"            if (text_hit == 0 && py >= line2_top && py < line2_top + char_h) {\n"
"                int lx = px - line2_left;\n"
"                if (lx >= 0 && lx < line2_len * char_w) {\n"
"                    int ci = lx / char_w;\n"
"                    int cxx = (lx % char_w) / cell;\n"
"                    int cy = (py - line2_top) / cell;\n"
"                    if (cxx < 8) {\n"
"                        int ch = 0;\n"
"                        switch (ci) {\n"
"                            case 0: ch=49; break; case 1: ch=56; break;\n"
"                            case 2: ch=32; break; case 3: ch=71; break;\n"
"                            case 4: ch=82; break; case 5: ch=65; break;\n"
"                            case 6: ch=89; break; case 7: ch=32; break;\n"
"                            case 8: ch=61; break; case 9: ch=32; break;\n"
"                            case 10: ch=48; break; case 11: ch=46; break;\n"
"                            case 12: ch=48+mg_d1; break;\n"
"                            case 13: ch=48+mg_d2; break;\n"
"                            case 14: ch=48+mg_d3; break;\n"
"                        }\n"
"                        if (ch > 32) {\n"
"                            int mask = font_8x12(ch, cy);\n"
"                            if ((mask >> (7 - cxx)) & 1) text_hit = 1;\n"
"                        }\n"
"                    }\n"
"                }\n"
"            }\n"
"            if (text_hit == 1) {\n"
"                val = mid_grey + 0.12f;\n"
"            } else if (in_box == 1) {\n"
"                val = mid_grey;\n"
"            }\n"
"        }\n"
"        p_Output[idx + 0] = val;\n"
"        p_Output[idx + 1] = val;\n"
"        p_Output[idx + 2] = val;\n"
"        p_Output[idx + 3] = 1.0f;\n"
"        return;\n"
"    }\n"
"\n"

// --- Tonescale overlay: init before grading so curve reflects all stages ---
"    float crv_tsn = 0.0f;\n"
"    float pos_x = (float)id.x;\n"
"    float pos_y = (float)id.y;\n"
"    float res_x = (float)params.width;\n"
"    float res_y = (float)params.height;\n"
"    if (params.showOverlay) {\n"
"        float cx = 1.0f - pos_x / res_x;\n"
"        crv_tsn = cx < 0.075f ? (cx - 0.075f) / 16.1844f\n"
"            : exp((cx - 0.552013f) / 0.0923290f) - 0.00570482f;\n"
"    }\n"
"\n"

// --- Decode to scene-linear ---
"    r = decode(r, params.inputTF);\n"
"    g = decode(g, params.inputTF);\n"
"    b = decode(b, params.inputTF);\n"
"\n"
"    // Exposure\n"
"    float gain = pow(2.0f, params.exposure);\n"
"    r *= gain; g *= gain; b *= gain;\n"
"    if (params.showOverlay) crv_tsn *= gain;\n"
"\n"
"    // Black Point\n"
"    if (params.blackPoint != 0.0f) {\n"
"        float knee = 0.005f;\n"
"        float bp = -params.blackPoint;\n"
"        r = soft_black_point(r, bp, knee);\n"
"        g = soft_black_point(g, bp, knee);\n"
"        b = soft_black_point(b, bp, knee);\n"
"        if (params.showOverlay) crv_tsn = soft_black_point(crv_tsn, bp, knee);\n"
"    }\n"
"\n"
"    // Color Balance\n"
"    r *= pow(2.0f,  params.temp);\n"
"    b *= pow(2.0f, -params.temp);\n"
"    g *= pow(2.0f,  params.tint);\n"
"\n"
"    // Encode back to log\n"
"    r = encode(r, params.inputTF);\n"
"    g = encode(g, params.inputTF);\n"
"    b = encode(b, params.inputTF);\n"
"    if (params.showOverlay) crv_tsn = encode(crv_tsn, params.inputTF);\n"
"\n"

// --- Log-domain operations ---
"    if (params.contrast != 1.0f || params.pivot != 0.0f) {\n"
"        float pvt = encode(0.18f, params.inputTF) + params.pivot;\n"
"        pvt = max(0.01f, min(pvt, 0.99f));\n"
"        r = rolling_contrast(r, params.contrast, pvt);\n"
"        g = rolling_contrast(g, params.contrast, pvt);\n"
"        b = rolling_contrast(b, params.contrast, pvt);\n"
"        if (params.showOverlay) crv_tsn = rolling_contrast(crv_tsn, params.contrast, pvt);\n"
"    }\n"
"\n"
"    if (params.shadows != 0.0f) {\n"
"        float mid = encode(0.18f, params.inputTF);\n"
"        float sgain = pow(2.0f, params.shadows);\n"
"        r = shadow_fill(r, sgain, mid);\n"
"        g = shadow_fill(g, sgain, mid);\n"
"        b = shadow_fill(b, sgain, mid);\n"
"        if (params.showOverlay) crv_tsn = shadow_fill(crv_tsn, sgain, mid);\n"
"    }\n"
"\n"
"    if (params.highlights != 0.0f) {\n"
"        float mid = encode(0.18f, params.inputTF);\n"
"        float hgain = pow(2.0f, params.highlights);\n"
"        r = highlight_gain(r, hgain, mid);\n"
"        g = highlight_gain(g, hgain, mid);\n"
"        b = highlight_gain(b, hgain, mid);\n"
"        if (params.showOverlay) crv_tsn = highlight_gain(crv_tsn, hgain, mid);\n"
"    }\n"
"\n"
"    if (params.rollOff > 0.0f) {\n"
"        float mid = encode(0.18f, params.inputTF);\n"
"        r = soft_highlight(r, params.rollOff, mid);\n"
"        g = soft_highlight(g, params.rollOff, mid);\n"
"        b = soft_highlight(b, params.rollOff, mid);\n"
"        if (params.showOverlay) crv_tsn = soft_highlight(crv_tsn, params.rollOff, mid);\n"
"    }\n"
"\n"
"    if (params.saturation != 0.0f && r >= 0.0f && g >= 0.0f && b >= 0.0f) {\n"
"        float luma_before = luminance(r, g, b);\n"
"        if (params.saturation > 0.0f) {\n"
"            float3 hsv = rgb_to_hsv(float3(r, g, b));\n"
"            hsv.y *= pow(2.0f, params.saturation);\n"
"            hsv.y = min(hsv.y, 1.0f);\n"
"            float3 rgb_sat = hsv_to_rgb(hsv);\n"
"            r = rgb_sat.x; g = rgb_sat.y; b = rgb_sat.z;\n"
"        } else {\n"
"            float factor = 1.0f + params.saturation;\n"
"            float lum = luma_before;\n"
"            r = lum + (r - lum) * factor;\n"
"            g = lum + (g - lum) * factor;\n"
"            b = lum + (b - lum) * factor;\n"
"        }\n"
"        if (params.preserveLuma) {\n"
"            float luma_after = luminance(r, g, b);\n"
"            if (luma_after > 0.0f && luma_before > 0.0f) {\n"
"                float ratio = luma_before / luma_after;\n"
"                r *= ratio; g *= ratio; b *= ratio;\n"
"            }\n"
"        }\n"
"    }\n"
"\n"

// --- Per-color tetrahedral ---
"    if (r >= 0.0f && g >= 0.0f && b >= 0.0f &&\n"
"        (params.redHue != 0.0f || params.redDen != 0.0f ||\n"
"         params.grnHue != 0.0f || params.grnDen != 0.0f ||\n"
"         params.bluHue != 0.0f || params.bluDen != 0.0f ||\n"
"         params.cynHue != 0.0f || params.cynDen != 0.0f ||\n"
"         params.magHue != 0.0f || params.magDen != 0.0f ||\n"
"         params.yelHue != 0.0f || params.yelDen != 0.0f)) {\n"
"        float3 rc = tetra_corner(float3(1,0,0), params.redHue, params.redDen);\n"
"        float3 gc = tetra_corner(float3(0,1,0), params.grnHue, params.grnDen);\n"
"        float3 bc = tetra_corner(float3(0,0,1), params.bluHue, params.bluDen);\n"
"        float3 cc = tetra_corner(float3(0,1,1), params.cynHue, params.cynDen);\n"
"        float3 mc = tetra_corner(float3(1,0,1), params.magHue, params.magDen);\n"
"        float3 yc = tetra_corner(float3(1,1,0), params.yelHue, params.yelDen);\n"
"        float3 result = tetra_interp(r, g, b, rc, gc, bc, cc, mc, yc);\n"
"        r = result.x; g = result.y; b = result.z;\n"
"    }\n"
"\n"

// =========================================================================
// OpenDRT Display Rendering (full perceptual pipeline v1.1.0)
// =========================================================================
"    if (params.enableDRT) {\n"
"        const float SQRT3 = 1.7320508075688772f;\n"
"        const float PI = 3.14159265358979f;\n"
"\n"
"        // Decode graded log back to scene-linear\n"
"        r = decode(r, params.inputTF);\n"
"        g = decode(g, params.inputTF);\n"
"        b = decode(b, params.inputTF);\n"
"        if (params.showOverlay) crv_tsn = decode(crv_tsn, params.inputTF);\n"
"\n"
"        float3 drt = float3(r, g, b);\n"
"\n"
"        // Input gamut -> P3-D65\n"
"        drt = mtx_mul(params.mtxInToWork, drt);\n"
"\n"
"        // Rendering Space saturation\n"
"        float3 rs_w = float3(params.rs_rw, 1.0f - params.rs_rw - params.rs_bw, params.rs_bw);\n"
"        float sat_L = drt.x * rs_w.x + drt.y * rs_w.y + drt.z * rs_w.z;\n"
"        drt = sat_L * params.rs_sa + drt * (1.0f - params.rs_sa);\n"
"\n"
"        // Offset\n"
"        drt += params.tn_off;\n"
"        if (params.showOverlay) crv_tsn += params.tn_off;\n"
"\n"
"        // Tonescale Norm\n"
"        float tsn = sqrt(max(0.0f, drt.x*drt.x + drt.y*drt.y + drt.z*drt.z)) / SQRT3;\n"
"\n"
"        // RGB Ratios\n"
"        drt = sdivf3f(drt, tsn);\n"
"\n"
"        // Opponent color space\n"
"        float2 opp = opponent(drt);\n"
"        float ach_d = length(opp) / 2.0f;\n"
"        ach_d = 1.25f * compress_toe_quadratic(ach_d, 0.25f, 0);\n"
"\n"
"        // Hue angle (rotated so red = 0)\n"
"        float hue = fmod(atan2(opp.x, opp.y) + PI + 1.10714931f, 2.0f * PI);\n"
"\n"
"        // RGB Hue Angles\n"
"        float3 ha_rgb = float3(\n"
"            gauss_window(hue_offset(hue, 0.1f), 0.66f),\n"
"            gauss_window(hue_offset(hue, 4.3f), 0.66f),\n"
"            gauss_window(hue_offset(hue, 2.3f), 0.66f));\n"
"\n"
"        // RGB Hue Angles for hue shift\n"
"        float3 ha_rgb_hs = float3(\n"
"            gauss_window(hue_offset(hue, -0.4f), 0.66f),\n"
"            ha_rgb.y,\n"
"            gauss_window(hue_offset(hue, 2.5f), 0.66f));\n"
"\n"
"        // CMY Hue Angles\n"
"        float3 ha_cmy = float3(\n"
"            gauss_window(hue_offset(hue, 3.3f), 0.5f),\n"
"            gauss_window(hue_offset(hue, 1.3f), 0.5f),\n"
"            gauss_window(hue_offset(hue, -1.15f), 0.5f));\n"
"\n"

// --- Brilliance (pre-tonescale) ---
"        if (params.brl_enable) {\n"
"            float brl_tsf = pow(tsn / (tsn + 1.0f), 1.0f - params.brl_rng);\n"
"            float brl_exf = (params.brl + params.brl_r * ha_rgb.x + params.brl_g * ha_rgb.y + params.brl_b * ha_rgb.z) * pow(ach_d, 1.0f / params.brl_st);\n"
"            float brl_ex = pow(2.0f, brl_exf * (brl_exf < 0.0f ? brl_tsf : 1.0f - brl_tsf));\n"
"            tsn *= brl_ex;\n"
"        }\n"
"\n"

// --- Contrast Low ---
"        if (params.tn_lcon_enable) {\n"
"            tsn *= params.lcon_cnst_sc;\n"
"            tsn = compress_toe_cubic(tsn, params.lcon_m, params.lcon_w, 0);\n"
"            if (params.showOverlay) crv_tsn = compress_toe_cubic(crv_tsn * params.lcon_cnst_sc, params.lcon_m, params.lcon_w, 0);\n"
"        }\n"
"\n"

// --- Contrast High ---
"        if (params.tn_hcon_enable) {\n"
"            tsn = contrast_high(tsn, params.hcon_p, params.tn_hcon_pv, params.tn_hcon_st, 0);\n"
"            if (params.showOverlay) crv_tsn = contrast_high(crv_tsn, params.hcon_p, params.tn_hcon_pv, params.tn_hcon_st, 0);\n"
"        }\n"
"\n"

// --- Hyperbolic Compression (3 versions) ---
"        float tsn_pt = compress_hyperbolic_power(tsn, params.ts_s1, params.ts_p);\n"
"        float tsn_const = compress_hyperbolic_power(tsn, params.s_Lp100, params.ts_p);\n"
"        tsn = compress_hyperbolic_power(tsn, params.ts_s, params.ts_p);\n"
"        float crv_tsn_const = 0.0f;\n"
"        if (params.showOverlay) {\n"
"            crv_tsn_const = compress_hyperbolic_power(crv_tsn, params.s_Lp100, params.ts_p);\n"
"            crv_tsn = compress_hyperbolic_power(crv_tsn, params.ts_s, params.ts_p);\n"
"        }\n"
"\n"

// --- Hue Contrast R ---
"        if (params.hc_enable) {\n"
"            float hc_ts = 1.0f - tsn_const;\n"
"            float hc_c = hc_ts * (1.0f - ach_d) + ach_d * (1.0f - hc_ts);\n"
"            hc_c *= ach_d * ha_rgb.x;\n"
"            hc_ts = pow(hc_ts, 1.0f / params.hc_r_rng);\n"
"            float hc_f = params.hc_r * (hc_c - 2.0f * hc_c * hc_ts) + 1.0f;\n"
"            drt = float3(drt.x, drt.y * hc_f, drt.z * hc_f);\n"
"        }\n"
"\n"

// --- Hue Shift RGB ---
"        if (params.hs_rgb_enable) {\n"
"            float3 hs_rgb = float3(\n"
"                ha_rgb_hs.x * ach_d * pow(tsn_pt, 1.0f / params.hs_r_rng),\n"
"                ha_rgb_hs.y * ach_d * pow(tsn_pt, 1.0f / params.hs_g_rng),\n"
"                ha_rgb_hs.z * ach_d * pow(tsn_pt, 1.0f / params.hs_b_rng));\n"
"            float3 hsf = float3(hs_rgb.x * params.hs_r, hs_rgb.y * -params.hs_g, hs_rgb.z * -params.hs_b);\n"
"            hsf = float3(hsf.z - hsf.y, hsf.x - hsf.z, hsf.y - hsf.x);\n"
"            drt += hsf;\n"
"        }\n"
"\n"

// --- Hue Shift CMY ---
"        if (params.hs_cmy_enable) {\n"
"            float tsn_pt_compl = 1.0f - tsn_pt;\n"
"            float3 hs_cmy = float3(\n"
"                ha_cmy.x * ach_d * pow(tsn_pt_compl, 1.0f / params.hs_c_rng),\n"
"                ha_cmy.y * ach_d * pow(tsn_pt_compl, 1.0f / params.hs_m_rng),\n"
"                ha_cmy.z * ach_d * pow(tsn_pt_compl, 1.0f / params.hs_y_rng));\n"
"            float3 hsf = float3(hs_cmy.x * -params.hs_c, hs_cmy.y * params.hs_m, hs_cmy.z * params.hs_y);\n"
"            hsf = float3(hsf.z - hsf.y, hsf.x - hsf.z, hsf.y - hsf.x);\n"
"            drt += hsf;\n"
"        }\n"
"\n"

// --- Purity Compression ---
"        if (params.pt_enable) {\n"
"            float pt_lml_p = 1.0f + 4.0f * (1.0f - tsn_pt) * (params.pt_lml + params.pt_lml_r * ha_rgb_hs.x + params.pt_lml_g * ha_rgb_hs.y + params.pt_lml_b * ha_rgb_hs.z);\n"
"            float ptf = 1.0f - pow(tsn_pt, pt_lml_p);\n"
"            float pt_lmh_p = (1.0f - ach_d * (params.pt_lmh_r * ha_rgb_hs.x + params.pt_lmh_b * ha_rgb_hs.z)) * (1.0f - params.pt_lmh * ach_d);\n"
"            ptf = pow(ptf, pt_lmh_p);\n"
"\n"

// --- Mid-Range Purity ---
"            if (params.ptm_enable) {\n"
"                float ptm_low_f;\n"
"                if (params.ptm_low_st == 0.0f || params.ptm_low_rng == 0.0f) ptm_low_f = 1.0f;\n"
"                else ptm_low_f = 1.0f + params.ptm_low * exp(-2.0f * ach_d * ach_d / params.ptm_low_st) * pow(1.0f - tsn_const, 1.0f / params.ptm_low_rng);\n"
"                float ptm_high_f;\n"
"                if (params.ptm_high_st == 0.0f || params.ptm_high_rng == 0.0f) ptm_high_f = 1.0f;\n"
"                else ptm_high_f = 1.0f + params.ptm_high * exp(-2.0f * ach_d * ach_d / params.ptm_high_st) * pow(tsn_pt, 1.0f / (4.0f * params.ptm_high_rng));\n"
"                ptf *= ptm_low_f * ptm_high_f;\n"
"            }\n"
"\n"
"            drt = drt * ptf + 1.0f - ptf;\n"
"        }\n"
"\n"

// --- Inverse Rendering Space ---
"        sat_L = drt.x * rs_w.x + drt.y * rs_w.y + drt.z * rs_w.z;\n"
"        drt = (sat_L * params.rs_sa - drt) / (params.rs_sa - 1.0f);\n"
"\n"

// --- Display Gamut + Whitepoint ---
"        drt = display_gamut_whitepoint(drt, tsn_const, params.cwp_lm, params.displayGamut, params.cwp);\n"
"\n"

// --- Post Brilliance ---
"        if (params.brlp_enable) {\n"
"            float2 brlp_opp = opponent(drt);\n"
"            float brlp_ach_d = length(brlp_opp) / 4.0f;\n"
"            brlp_ach_d = 1.1f * (brlp_ach_d * brlp_ach_d / (brlp_ach_d + 0.1f));\n"
"            float3 brlp_ha_rgb = ach_d * ha_rgb;\n"
"            float brlp_m = params.brlp + params.brlp_r * brlp_ha_rgb.x + params.brlp_g * brlp_ha_rgb.y + params.brlp_b * brlp_ha_rgb.z;\n"
"            float brlp_ex = pow(2.0f, brlp_m * brlp_ach_d * tsn);\n"
"            drt *= brlp_ex;\n"
"        }\n"
"\n"

// --- Purity Compress Low (softplus) ---
"        if (params.ptl_enable) {\n"
"            drt = float3(softplus(drt.x, params.ptl_c), softplus(drt.y, params.ptl_m), softplus(drt.z, params.ptl_y));\n"
"        }\n"
"\n"

// --- Final Tonescale ---
"        tsn *= params.ts_m2;\n"
"        tsn = compress_toe_quadratic(tsn, params.tn_toe, 0);\n"
"        tsn *= params.ts_dsc;\n"
"        if (params.showOverlay) {\n"
"            crv_tsn *= params.ts_m2;\n"
"            crv_tsn = compress_toe_quadratic(crv_tsn, params.tn_toe, 0);\n"
"            crv_tsn *= params.ts_dsc;\n"
"        }\n"
"\n"

// --- Return from RGB ratios ---
"        drt *= tsn;\n"
"\n"

// --- Rec.2020 conversion ---
"        if (params.displayGamut == 2) {\n"
"            drt = clampminf3(drt, 0.0f);\n"
"            drt = mtx_mul_c(mtx_p3_to_rec2020, drt);\n"
"        }\n"
"\n"

// --- Clamp ---
"        drt = clampf3(drt, 0.0f, 1.0f);\n"
"\n"

// --- EOTF encode ---
"        float eotf_p = 2.0f + (float)params.eotf * 0.2f;\n"
"        if (params.eotf > 0 && params.eotf < 4) {\n"
"            drt = pow(max(drt, float3(0.0f)), float3(1.0f / eotf_p));\n"
"        } else if (params.eotf == 4) {\n"
"            drt = eotf_pq_encode(drt);\n"
"        } else if (params.eotf == 5) {\n"
"            drt = eotf_hlg_encode(drt);\n"
"        }\n"
"\n"
"        r = drt.x; g = drt.y; b = drt.z;\n"
"\n"
"        // Tonescale curve overlay (GPU-rendered, matches DCTL approach)\n"
"        if (params.showOverlay) {\n"
"            float3 crv_rgb = float3(crv_tsn, crv_tsn, crv_tsn);\n"
"            crv_rgb = display_gamut_whitepoint(crv_rgb, crv_tsn_const, params.cwp_lm, params.displayGamut, params.cwp);\n"
"            if (params.displayGamut == 2) {\n"
"                crv_rgb = clampminf3(crv_rgb, 0.0f);\n"
"                crv_rgb = mtx_mul_c(mtx_p3_to_rec2020, crv_rgb);\n"
"            }\n"
"            crv_rgb = clampf3(crv_rgb, 0.0f, 1.0f);\n"
"            if (params.eotf > 0 && params.eotf < 4) {\n"
"                crv_rgb = pow(max(crv_rgb, float3(0.0f)), float3(1.0f / eotf_p));\n"
"            } else if (params.eotf == 4) {\n"
"                crv_rgb = eotf_pq_encode(crv_rgb);\n"
"            } else if (params.eotf == 5) {\n"
"                crv_rgb = eotf_hlg_encode(crv_rgb);\n"
"            }\n"
"            float3 crv_dst = float3(\n"
"                res_y - pos_y - crv_rgb.x * res_y,\n"
"                res_y - pos_y - crv_rgb.y * res_y,\n"
"                res_y - pos_y - crv_rgb.z * res_y);\n"
"            float crv_w = 0.35f;\n"
"            crv_dst = float3(\n"
"                exp(-crv_dst.x * crv_dst.x * crv_w),\n"
"                exp(-crv_dst.y * crv_dst.y * crv_w),\n"
"                exp(-crv_dst.z * crv_dst.z * crv_w));\n"
"            crv_dst = clampf3(crv_dst, 0.0f, 1.0f);\n"
"            float3 out_rgb = float3(r, g, b);\n"
"            out_rgb = out_rgb * (1.0f - crv_dst) + crv_dst;\n"
"            r = out_rgb.x; g = out_rgb.y; b = out_rgb.z;\n"
"        }\n"
"    }\n"
"\n"
"    p_Output[idx + 0] = r;\n"
"    p_Output[idx + 1] = g;\n"
"    p_Output[idx + 2] = b;\n"
"    p_Output[idx + 3] = a;\n"
"}\n";

// ============================================================================
// Pipeline cache
// ============================================================================
std::mutex s_PipelineQueueMutex;
typedef std::unordered_map<id<MTLCommandQueue>, id<MTLComputePipelineState>> PipelineQueueMap;
PipelineQueueMap s_PipelineQueueMap;

void RunMetalKernel(void* p_CmdQ, int p_Width, int p_Height,
                    const PrimeraGPUParams* p_Params,
                    const float* p_Input, float* p_Output)
{
    id<MTLCommandQueue> queue = static_cast<id<MTLCommandQueue>>(p_CmdQ);
    id<MTLDevice> device = queue.device;
    id<MTLComputePipelineState> pipelineState;

    std::unique_lock<std::mutex> lock(s_PipelineQueueMutex);

    const auto it = s_PipelineQueueMap.find(queue);
    if (it == s_PipelineQueueMap.end())
    {
        NSError* err = nil;

        MTLCompileOptions* options = [MTLCompileOptions new];
        options.mathMode = MTLMathModeFast;

        id<MTLLibrary> metalLibrary = [device newLibraryWithSource:@(kernelSource) options:options error:&err];
        [options release];
        if (!metalLibrary)
        {
            fprintf(stderr, "Primera OFX: failed to compile Metal library: %s\n",
                    err.localizedDescription.UTF8String);
            return;
        }

        id<MTLFunction> kernelFunction = [metalLibrary newFunctionWithName:@"PrimeraKernel"];
        [metalLibrary release];
        if (!kernelFunction)
        {
            fprintf(stderr, "Primera OFX: failed to find PrimeraKernel function\n");
            return;
        }

        pipelineState = [device newComputePipelineStateWithFunction:kernelFunction error:&err];
        [kernelFunction release];
        if (!pipelineState)
        {
            fprintf(stderr, "Primera OFX: failed to create pipeline state: %s\n",
                    err.localizedDescription.UTF8String);
            return;
        }

        s_PipelineQueueMap[queue] = pipelineState;
    }
    else
    {
        pipelineState = it->second;
    }

    id<MTLBuffer> srcDeviceBuf = reinterpret_cast<id<MTLBuffer>>(const_cast<float*>(p_Input));
    id<MTLBuffer> dstDeviceBuf = reinterpret_cast<id<MTLBuffer>>(p_Output);

    id<MTLCommandBuffer> commandBuffer = [queue commandBuffer];
    commandBuffer.label = @"PrimeraKernel";

    id<MTLComputeCommandEncoder> computeEncoder = [commandBuffer computeCommandEncoder];
    [computeEncoder setComputePipelineState:pipelineState];

    [computeEncoder setBuffer:srcDeviceBuf offset:0 atIndex:0];
    [computeEncoder setBuffer:dstDeviceBuf offset:0 atIndex:8];
    [computeEncoder setBytes:p_Params length:sizeof(PrimeraGPUParams) atIndex:1];

    int exeWidth = [pipelineState threadExecutionWidth];
    MTLSize threadGroupCount = MTLSizeMake(exeWidth, 1, 1);
    MTLSize threadGroups = MTLSizeMake((p_Width + exeWidth - 1) / exeWidth, p_Height, 1);

    [computeEncoder dispatchThreadgroups:threadGroups threadsPerThreadgroup:threadGroupCount];
    [computeEncoder endEncoding];
    [commandBuffer commit];
}
