"float3 rgb_to_hsv(float3 rgb) {\n"
"    float r = rgb.x, g = rgb.y, b = rgb.z;\n"
"    float cmax = max(r, max(g, b));\n"
"    float cmin = min(r, min(g, b));\n"
"    float delta = cmax - cmin;\n"
"    float h = 0.0f;\n"
"    if (delta > 0.0f) {\n"
"        if (cmax == r)      h = (g - b) / delta;\n"
"        else if (cmax == g) h = (b - r) / delta + 2.0f;\n"
"        else                h = (r - g) / delta + 4.0f;\n"
"        if (h < 0.0f) h += 6.0f;\n"
"        h /= 6.0f;\n"
"    }\n"
"    float s = (cmax > 0.0f) ? delta / cmax : 0.0f;\n"
"    return float3(h, s, cmax);\n"
"}\n"
